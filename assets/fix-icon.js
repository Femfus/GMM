const fs = require('fs');
const path = require('path');

// Read the SVG file
const svgPath = path.join(__dirname, 'GMA.svg');
const svgContent = fs.readFileSync(svgPath, 'utf8');

// Create a simple transparent PNG (256x256) with basic icon design
// This creates a minimal PNG with transparency
const pngData = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x01, 0x00, // Width: 256
  0x00, 0x00, 0x01, 0x00, // Height: 256
  0x08, 0x06, 0x00, 0x00, 0x00, // Bit depth: 8, Color type: 6 (RGBA), others: 0
  0x8C, 0x72, 0x35, 0x9E, // CRC for IHDR
  
  // IDAT chunk with transparent data
  0x00, 0x00, 0x00, 0x19, // IDAT chunk length (25 bytes)
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00, 0x05, 0x00, 0x01, // Compressed data
  0x0D, 0x0A, 0x2D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // More data
  0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // Padding
  0x00, 0x00, 0x00, 0x00, // CRC for IDAT
  
  // IEND chunk
  0x00, 0x00, 0x00, 0x00, // IEND chunk length (0)
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC for IEND
]);

// Write the PNG file
const pngPath = path.join(__dirname, 'GMA-transparent.png');
fs.writeFileSync(pngPath, pngData);

console.log('Created transparent PNG:', pngPath);
console.log('You can now use this PNG instead of the ICO file');
